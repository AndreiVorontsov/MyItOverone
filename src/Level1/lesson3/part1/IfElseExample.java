package Level1.lesson3.part1;

public class IfElseExample {
    public static void main(String[] args) {
        /*З-ча с if else:
- создать новый класс
- создать boolean переменную hasVisa и присвоить любое значение
используя “if else” проверить если есть виза, вывести “Едем за границу”, иначе вывести “Путешествуем по Беларуси”.*/

        boolean hasVisa = false;
        if (hasVisa) {
            System.out.println("Едем за границу");
        } else {
            System.out.println("Путешествуем по Беларуси");
        }

    }
}
